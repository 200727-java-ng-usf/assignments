package com.revature.util;

public class FloatPoints {
    private float x = 42.0F;
    private float y = 31.0F;

    public FloatPoints() {
        super();
    }

    public FloatPoints(float x, float y) {
        this.x = x;
        this.y = y;
    }

    public float getX() {
        return x;
    }

    public void setX(float x) {
        this.x = x;
    }

    public float getY() {
        return y;
    }

    public void setY(float y) {
        this.y = y;
    }
}
